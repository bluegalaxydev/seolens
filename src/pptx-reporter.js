/**
 * PowerPoint (.pptx) report generator for Seolens.
 *
 * Builds a branded slide deck from an audit result:
 *  - Slide 1: Cover (URL, score, grade, date)
 *  - Slide 2: Executive summary (counts, biggest wins/issues)
 *  - Slide 3: Score breakdown by category
 *  - Slide 4..N: One slide per failed check with the "how to fix" guidance
 *  - Final slide: What to do next + Pro upsell
 *
 * Uses pptxgenjs — pure JS, no external runtime required.
 */
import pptxgen from 'pptxgenjs';
import { categories } from './checks/index.js';

const COLOR = {
  bg: 'FFFFFF',
  ink: '1A1A1A',
  muted: '6B7280',
  primary: '0066FF',
  green: '16A34A',
  red: 'DC2626',
  yellow: 'CA8A04',
  blue: '2563EB',
  gray: 'F3F4F6',
};

export async function renderPptx(audit, outPath) {
  const pres = new pptxgen();
  pres.layout = 'LAYOUT_WIDE'; // 13.33 x 7.5 inches
  pres.author = 'Seolens';
  pres.title = `SEO Audit — ${audit.url}`;

  coverSlide(pres, audit);
  summarySlide(pres, audit);
  categoryBreakdownSlide(pres, audit);
  issueSlides(pres, audit);
  passedSlide(pres, audit);
  closingSlide(pres, audit);

  await pres.writeFile({ fileName: outPath });
  return outPath;
}

function coverSlide(pres, audit) {
  const s = pres.addSlide();
  s.background = { color: COLOR.bg };

  // Brand strip
  s.addShape(pres.shapes.RECTANGLE, { x: 0, y: 0, w: 13.33, h: 0.5, fill: { color: COLOR.primary }, line: { color: COLOR.primary } });
  s.addText('SEOLENS', { x: 0.5, y: 0.05, w: 12, h: 0.4, fontSize: 14, bold: true, color: 'FFFFFF', fontFace: 'Helvetica' });

  // Title
  s.addText('SEO Audit Report', {
    x: 0.5, y: 1.0, w: 12, h: 0.8,
    fontSize: 40, bold: true, color: COLOR.ink, fontFace: 'Helvetica',
  });

  s.addText(audit.url, {
    x: 0.5, y: 1.9, w: 12, h: 0.5,
    fontSize: 20, color: COLOR.muted, fontFace: 'Helvetica',
  });

  // Big score
  const scoreColor = audit.score.value >= 80 ? COLOR.green : audit.score.value >= 60 ? COLOR.yellow : COLOR.red;
  s.addShape(pres.shapes.ROUNDED_RECTANGLE, {
    x: 0.5, y: 3.0, w: 5.5, h: 3.5,
    fill: { color: COLOR.gray }, line: { color: COLOR.gray }, rectRadius: 0.2,
  });
  s.addText(`${audit.score.value}`, {
    x: 0.5, y: 3.0, w: 5.5, h: 2.5,
    fontSize: 130, bold: true, color: scoreColor, align: 'center', valign: 'middle', fontFace: 'Helvetica',
  });
  s.addText(`/100  ·  Grade ${audit.score.grade}  ·  ${audit.score.label}`, {
    x: 0.5, y: 5.5, w: 5.5, h: 0.6,
    fontSize: 18, color: COLOR.ink, align: 'center', fontFace: 'Helvetica',
  });

  // Stat strip
  const stats = [
    { label: 'Passed', value: audit.summary.passed, color: COLOR.green },
    { label: 'Critical', value: audit.summary.critical, color: COLOR.red },
    { label: 'Warning', value: audit.summary.warning, color: COLOR.yellow },
    { label: 'Info', value: audit.summary.info, color: COLOR.blue },
  ];
  stats.forEach((stat, i) => {
    const x = 6.5 + i * 1.6;
    s.addShape(pres.shapes.ROUNDED_RECTANGLE, { x, y: 3.0, w: 1.4, h: 1.6, fill: { color: COLOR.gray }, line: { color: COLOR.gray }, rectRadius: 0.1 });
    s.addText(`${stat.value}`, { x, y: 3.0, w: 1.4, h: 1.0, fontSize: 36, bold: true, color: stat.color, align: 'center', valign: 'middle', fontFace: 'Helvetica' });
    s.addText(stat.label, { x, y: 4.0, w: 1.4, h: 0.5, fontSize: 12, color: COLOR.muted, align: 'center', fontFace: 'Helvetica' });
  });

  // Audited at
  s.addText(`Audited: ${new Date(audit.fetchedAt).toLocaleString()}`, {
    x: 6.5, y: 5.0, w: 6.5, h: 0.4, fontSize: 11, color: COLOR.muted, fontFace: 'Helvetica',
  });

  // Footer
  s.addText('Generated by Seolens · github.com/bluegalaxydev/seolens', {
    x: 0.5, y: 7.0, w: 12, h: 0.3, fontSize: 10, color: COLOR.muted, align: 'center', fontFace: 'Helvetica',
  });
}

function summarySlide(pres, audit) {
  const s = pres.addSlide();
  s.background = { color: COLOR.bg };
  addHeader(s, pres, 'Executive Summary');

  const failed = audit.results.filter((r) => !r.passed && r.severity !== 'skip');
  const top = failed.sort((a, b) => severityRank(b.severity) - severityRank(a.severity)).slice(0, 5);

  s.addText('Top issues to fix first', { x: 0.5, y: 1.2, w: 12, h: 0.5, fontSize: 22, bold: true, color: COLOR.ink, fontFace: 'Helvetica' });

  if (top.length === 0) {
    s.addText('No issues found. Excellent work — focus on monitoring.', { x: 0.5, y: 2.0, w: 12, h: 0.6, fontSize: 18, color: COLOR.green, fontFace: 'Helvetica' });
  } else {
    const rows = [
      [
        { text: '#', options: { bold: true, fill: { color: COLOR.gray } } },
        { text: 'Category', options: { bold: true, fill: { color: COLOR.gray } } },
        { text: 'Issue', options: { bold: true, fill: { color: COLOR.gray } } },
        { text: 'Severity', options: { bold: true, fill: { color: COLOR.gray } } },
      ],
      ...top.map((r, i) => [
        { text: `${i + 1}`, options: { color: COLOR.muted } },
        { text: categories[r.category] || r.category, options: { color: COLOR.ink } },
        { text: r.label, options: { color: COLOR.ink } },
        { text: (r.severity || '').toUpperCase(), options: { color: severityColor(r.severity), bold: true } },
      ]),
    ];
    s.addTable(rows, { x: 0.5, y: 1.9, w: 12.3, fontSize: 13, fontFace: 'Helvetica', border: { type: 'solid', color: 'E5E7EB', pt: 0.5 }, colW: [0.6, 2.5, 7.5, 1.7] });
  }

  s.addText('What this score means', { x: 0.5, y: 5.5, w: 12, h: 0.5, fontSize: 16, bold: true, color: COLOR.ink, fontFace: 'Helvetica' });
  s.addText(scoreInterpretation(audit.score.value), {
    x: 0.5, y: 6.0, w: 12, h: 1.0, fontSize: 13, color: COLOR.muted, fontFace: 'Helvetica',
  });
}

function categoryBreakdownSlide(pres, audit) {
  const s = pres.addSlide();
  s.background = { color: COLOR.bg };
  addHeader(s, pres, 'Score by Category');

  const byCat = {};
  for (const r of audit.results) {
    if (r.severity === 'skip') continue;
    const cat = r.category;
    if (!byCat[cat]) byCat[cat] = { passed: 0, total: 0 };
    byCat[cat].total += r.weight;
    if (r.passed) byCat[cat].passed += r.weight;
  }

  const entries = Object.entries(byCat).map(([cat, stats]) => ({
    cat,
    label: categories[cat] || cat,
    pct: Math.round((stats.passed / stats.total) * 100),
  }));

  // Bar chart manually
  const top = 1.5;
  const rowH = 0.55;
  entries.forEach((e, i) => {
    const y = top + i * rowH;
    s.addText(e.label, { x: 0.5, y, w: 3, h: 0.4, fontSize: 14, color: COLOR.ink, fontFace: 'Helvetica' });
    // Bar bg
    s.addShape(pres.shapes.RECTANGLE, { x: 4, y: y + 0.05, w: 7, h: 0.3, fill: { color: COLOR.gray }, line: { color: COLOR.gray } });
    // Bar fill
    const fillW = (e.pct / 100) * 7;
    const barColor = e.pct >= 80 ? COLOR.green : e.pct >= 60 ? COLOR.yellow : COLOR.red;
    if (fillW > 0) {
      s.addShape(pres.shapes.RECTANGLE, { x: 4, y: y + 0.05, w: fillW, h: 0.3, fill: { color: barColor }, line: { color: barColor } });
    }
    s.addText(`${e.pct}%`, { x: 11.2, y, w: 1.5, h: 0.4, fontSize: 14, bold: true, color: COLOR.ink, fontFace: 'Helvetica' });
  });
}

function issueSlides(pres, audit) {
  const failed = audit.results.filter((r) => !r.passed && r.severity !== 'skip');
  if (failed.length === 0) return;

  // Group by category
  const byCat = {};
  for (const r of failed) {
    if (!byCat[r.category]) byCat[r.category] = [];
    byCat[r.category].push(r);
  }

  for (const [cat, items] of Object.entries(byCat)) {
    const s = pres.addSlide();
    s.background = { color: COLOR.bg };
    addHeader(s, pres, categories[cat] || cat);

    let y = 1.4;
    for (const r of items) {
      const sevColor = severityColor(r.severity);
      // Severity badge
      s.addShape(pres.shapes.ROUNDED_RECTANGLE, { x: 0.5, y, w: 1.0, h: 0.35, fill: { color: sevColor }, line: { color: sevColor }, rectRadius: 0.05 });
      s.addText((r.severity || 'fail').toUpperCase(), { x: 0.5, y, w: 1.0, h: 0.35, fontSize: 10, bold: true, color: 'FFFFFF', align: 'center', valign: 'middle', fontFace: 'Helvetica' });
      // Label
      s.addText(r.label, { x: 1.7, y: y - 0.02, w: 11, h: 0.4, fontSize: 16, bold: true, color: COLOR.ink, fontFace: 'Helvetica' });
      // Message
      s.addText(r.message || '', { x: 1.7, y: y + 0.4, w: 11, h: 0.6, fontSize: 12, color: COLOR.muted, fontFace: 'Helvetica' });
      // Fix
      if (r.fix) {
        s.addText('How to fix:', { x: 1.7, y: y + 0.95, w: 1.5, h: 0.3, fontSize: 11, bold: true, color: COLOR.primary, fontFace: 'Helvetica' });
        s.addText(r.fix, { x: 3.1, y: y + 0.95, w: 9.5, h: 0.5, fontSize: 11, color: COLOR.ink, fontFace: 'Helvetica' });
      }
      y += 1.6;
      if (y > 6.8) break; // overflow protection
    }
  }
}

function passedSlide(pres, audit) {
  const passed = audit.results.filter((r) => r.passed);
  if (passed.length === 0) return;
  const s = pres.addSlide();
  s.background = { color: COLOR.bg };
  addHeader(s, pres, `What's Working (${passed.length} checks passed)`);

  // 2 columns
  const half = Math.ceil(passed.length / 2);
  passed.slice(0, half).forEach((r, i) => {
    s.addText(`✓  ${r.label}`, { x: 0.5, y: 1.4 + i * 0.35, w: 6, h: 0.3, fontSize: 12, color: COLOR.green, fontFace: 'Helvetica' });
  });
  passed.slice(half).forEach((r, i) => {
    s.addText(`✓  ${r.label}`, { x: 6.8, y: 1.4 + i * 0.35, w: 6, h: 0.3, fontSize: 12, color: COLOR.green, fontFace: 'Helvetica' });
  });
}

function closingSlide(pres, audit) {
  const s = pres.addSlide();
  s.background = { color: COLOR.bg };
  addHeader(s, pres, 'Next Steps');

  s.addText('1.  Tackle critical issues first', { x: 0.5, y: 1.5, w: 12, h: 0.5, fontSize: 18, color: COLOR.ink, fontFace: 'Helvetica' });
  s.addText('Critical issues are the ones most likely to hurt your search rankings — fix these before warnings.', { x: 1.0, y: 2.0, w: 11, h: 0.5, fontSize: 12, color: COLOR.muted, fontFace: 'Helvetica' });

  s.addText('2.  Re-audit after each batch of fixes', { x: 0.5, y: 2.7, w: 12, h: 0.5, fontSize: 18, color: COLOR.ink, fontFace: 'Helvetica' });
  s.addText('Run `npx seolens <url>` after deploying changes to confirm the score improves.', { x: 1.0, y: 3.2, w: 11, h: 0.5, fontSize: 12, color: COLOR.muted, fontFace: 'Helvetica' });

  s.addText('3.  Add Seolens to your CI pipeline', { x: 0.5, y: 3.9, w: 12, h: 0.5, fontSize: 18, color: COLOR.ink, fontFace: 'Helvetica' });
  s.addText('Seolens exits non-zero when score < 70 — gate deploys on SEO regressions.', { x: 1.0, y: 4.4, w: 11, h: 0.5, fontSize: 12, color: COLOR.muted, fontFace: 'Helvetica' });

  // Pro upsell
  s.addShape(pres.shapes.ROUNDED_RECTANGLE, { x: 0.5, y: 5.5, w: 12.3, h: 1.5, fill: { color: COLOR.gray }, line: { color: COLOR.gray }, rectRadius: 0.15 });
  s.addText('Need more?', { x: 0.8, y: 5.6, w: 12, h: 0.4, fontSize: 14, bold: true, color: COLOR.primary, fontFace: 'Helvetica' });
  s.addText('Seolens Pro adds full-site crawl (up to 50,000 pages), Core Web Vitals, hreflang validation, AI fix suggestions, and white-label HTML reports.', { x: 0.8, y: 6.0, w: 12, h: 0.6, fontSize: 12, color: COLOR.ink, fontFace: 'Helvetica' });
  s.addText('→ github.com/bluegalaxydev/seolens-pro', { x: 0.8, y: 6.6, w: 12, h: 0.3, fontSize: 12, color: COLOR.primary, fontFace: 'Helvetica' });

  s.addText(`Generated by Seolens on ${new Date().toLocaleDateString()}`, { x: 0.5, y: 7.1, w: 12, h: 0.3, fontSize: 9, color: COLOR.muted, align: 'center', fontFace: 'Helvetica' });
}

function addHeader(slide, pres, title) {
  slide.addShape(pres.shapes.RECTANGLE, { x: 0, y: 0, w: 13.33, h: 0.5, fill: { color: COLOR.primary }, line: { color: COLOR.primary } });
  slide.addText('SEOLENS', { x: 0.5, y: 0.05, w: 6, h: 0.4, fontSize: 14, bold: true, color: 'FFFFFF', fontFace: 'Helvetica' });
  slide.addText(title, { x: 0.5, y: 0.7, w: 12.3, h: 0.5, fontSize: 24, bold: true, color: COLOR.ink, fontFace: 'Helvetica' });
}

function severityColor(s) {
  if (s === 'critical' || s === 'error') return COLOR.red;
  if (s === 'warning') return COLOR.yellow;
  if (s === 'info') return COLOR.blue;
  return COLOR.muted;
}

function severityRank(s) {
  if (s === 'critical' || s === 'error') return 3;
  if (s === 'warning') return 2;
  if (s === 'info') return 1;
  return 0;
}

function scoreInterpretation(s) {
  if (s >= 90) return 'Excellent on-page SEO. The site follows current best practices across meta tags, structure, and technical fundamentals. Focus on monitoring and incremental improvements.';
  if (s >= 80) return 'Solid SEO foundation. A few warnings to clean up but nothing that should significantly hurt rankings. Quick wins available.';
  if (s >= 70) return 'Acceptable but needs attention. Multiple warnings present. Fixing 3-5 high-impact items will likely move you to a B grade.';
  if (s >= 60) return 'Notable issues that may suppress organic visibility. Prioritize the critical findings — these often represent the highest ROI fixes.';
  return 'Significant on-page issues that are likely hurting search performance. Address critical findings immediately.';
}
