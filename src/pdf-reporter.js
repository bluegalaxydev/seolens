/**
 * PDF report generator (3-4 pages, compact) for Seolens.
 *
 * Always exactly 4 pages:
 *   Page 1: Cover — big score, URL, audited date, stats
 *   Page 2: Executive summary — top 5 issues with fixes
 *   Page 3: All findings — compact list grouped by category
 *   Page 4: What's working + next steps + Pro upsell
 *
 * Uses PDFKit (pure JS, no headless browser).
 */
import PDFDocument from 'pdfkit';
import { createWriteStream } from 'node:fs';
import { categories } from './checks/index.js';

const COLOR = {
  ink: '#1a1a1a',
  muted: '#6b7280',
  primary: '#0066ff',
  green: '#16a34a',
  red: '#dc2626',
  yellow: '#ca8a04',
  blue: '#2563eb',
  gray: '#f3f4f6',
  border: '#e5e7eb',
};

export async function renderPdf(audit, outPath) {
  return new Promise((resolve, reject) => {
    const doc = new PDFDocument({
      size: 'LETTER',
      margins: { top: 60, bottom: 60, left: 60, right: 60 },
      autoFirstPage: false,
      info: {
        Title: `SEO Audit — ${audit.url}`,
        Author: 'Seolens',
        Subject: `On-page SEO audit for ${audit.url}`,
        Producer: 'Seolens',
      },
    });

    const stream = createWriteStream(outPath);
    stream.on('finish', () => resolve(outPath));
    stream.on('error', reject);
    doc.pipe(stream);

    coverPage(doc, audit);
    summaryPage(doc, audit);
    findingsPage(doc, audit);
    closingPage(doc, audit);

    doc.end();
  });
}

// ---------- PAGE 1: COVER ----------
function coverPage(doc, audit) {
  doc.addPage();
  doc.rect(0, 0, doc.page.width, 8).fill(COLOR.primary);

  doc.fillColor(COLOR.muted).fontSize(11).font('Helvetica-Bold').text('SEOLENS', 60, 30);

  doc.fillColor(COLOR.ink).fontSize(32).font('Helvetica-Bold').text('SEO Audit Report', 60, 90);

  doc.fillColor(COLOR.muted).fontSize(13).font('Helvetica').text(audit.url, 60, 135, { width: 480 });

  doc.fillColor(COLOR.muted).fontSize(10).text(`Audited ${new Date(audit.fetchedAt).toLocaleString()}`, 60, 158);

  // Big score box
  const boxY = 200;
  doc.roundedRect(60, boxY, 480, 220, 12).fill(COLOR.gray);

  const scoreColor = audit.score.value >= 80 ? COLOR.green : audit.score.value >= 60 ? COLOR.yellow : COLOR.red;
  doc.fillColor(scoreColor).fontSize(120).font('Helvetica-Bold').text(`${audit.score.value}`, 60, boxY + 30, { width: 480, align: 'center' });
  doc.fillColor(COLOR.muted).fontSize(14).font('Helvetica').text('out of 100', 60, boxY + 165, { width: 480, align: 'center' });
  doc.fillColor(COLOR.ink).fontSize(18).font('Helvetica-Bold').text(`Grade ${audit.score.grade} · ${audit.score.label}`, 60, boxY + 187, { width: 480, align: 'center' });

  // Stats row
  const statsY = 450;
  const stats = [
    { label: 'Passed', value: audit.summary.passed, color: COLOR.green },
    { label: 'Critical', value: audit.summary.critical, color: COLOR.red },
    { label: 'Warning', value: audit.summary.warning, color: COLOR.yellow },
    { label: 'Info', value: audit.summary.info, color: COLOR.blue },
  ];
  const statW = 110;
  const startX = (doc.page.width - statW * 4 - 30) / 2;
  stats.forEach((stat, i) => {
    const x = startX + i * (statW + 10);
    doc.roundedRect(x, statsY, statW, 70, 6).fill(COLOR.gray);
    doc.fillColor(stat.color).fontSize(28).font('Helvetica-Bold').text(`${stat.value}`, x, statsY + 12, { width: statW, align: 'center' });
    doc.fillColor(COLOR.muted).fontSize(10).font('Helvetica').text(stat.label.toUpperCase(), x, statsY + 47, { width: statW, align: 'center', characterSpacing: 1 });
  });

  // Footer
  doc.fillColor(COLOR.muted).fontSize(9).text('Generated by Seolens · github.com/bluegalaxydev/seolens', 60, doc.page.height - 50, { width: doc.page.width - 120, align: 'center' });
}

// ---------- PAGE 2: EXECUTIVE SUMMARY ----------
function summaryPage(doc, audit) {
  doc.addPage();
  pageHeader(doc, 'Executive Summary');

  let y = 110;

  // Interpretation
  const interp = scoreInterpretation(audit.score.value);
  doc.fillColor(COLOR.ink).fontSize(11).font('Helvetica').text(interp, 60, y, { width: doc.page.width - 120, lineGap: 3 });
  y = doc.y + 20;

  // Top issues heading
  doc.fillColor(COLOR.ink).fontSize(15).font('Helvetica-Bold').text('Top issues to fix first', 60, y);
  y += 25;

  const failed = audit.results.filter((r) => !r.passed && r.severity !== 'skip');
  const top = failed.sort((a, b) => severityRank(b.severity) - severityRank(a.severity)).slice(0, 5);

  if (top.length === 0) {
    doc.fillColor(COLOR.green).fontSize(12).font('Helvetica').text('✓ No issues found. Excellent work — focus on monitoring.', 60, y);
  } else {
    top.forEach((r, i) => {
      const sev = (r.severity || 'fail').toUpperCase();
      const sevColor = severityColor(r.severity);

      // Number
      doc.circle(72, y + 10, 11).fill(COLOR.primary);
      doc.fillColor('white').fontSize(11).font('Helvetica-Bold').text(`${i + 1}`, 60, y + 5, { width: 24, align: 'center' });

      // Severity badge
      doc.roundedRect(95, y + 1, 56, 18, 3).fill(sevColor);
      doc.fillColor('white').fontSize(9).font('Helvetica-Bold').text(sev, 95, y + 5, { width: 56, align: 'center' });

      // Label
      doc.fillColor(COLOR.ink).fontSize(12).font('Helvetica-Bold').text(r.label, 160, y + 2, { width: 380, ellipsis: true });

      // Fix line
      const fixText = r.fix || r.message || '';
      doc.fillColor(COLOR.muted).fontSize(10).font('Helvetica').text(fixText, 160, y + 20, { width: 380, lineGap: 1, height: 26, ellipsis: true });

      y += 50;
    });
  }

  pageFooter(doc, 2);
}

// ---------- PAGE 3: ALL FINDINGS (COMPACT) ----------
function findingsPage(doc, audit) {
  doc.addPage();
  pageHeader(doc, 'All Findings');

  let y = 110;
  const failed = audit.results.filter((r) => !r.passed && r.severity !== 'skip');

  if (failed.length === 0) {
    doc.fillColor(COLOR.green).fontSize(13).text('All checks passed. No issues to address.', 60, y);
    pageFooter(doc, 3);
    return;
  }

  const grouped = groupBy(failed, 'category');
  const maxY = doc.page.height - 90;

  for (const [cat, items] of Object.entries(grouped)) {
    if (y > maxY - 40) break;

    // Category heading
    doc.fillColor(COLOR.primary).fontSize(12).font('Helvetica-Bold').text(categories[cat] || cat, 60, y);
    y += 18;

    for (const r of items) {
      if (y > maxY - 30) {
        doc.fillColor(COLOR.muted).fontSize(9).font('Helvetica-Oblique').text('... see full list in JSON or Markdown export', 60, y);
        y += 14;
        break;
      }
      const sev = (r.severity || 'fail').toUpperCase().slice(0, 4);
      const sevColor = severityColor(r.severity);
      doc.roundedRect(60, y, 32, 12, 2).fill(sevColor);
      doc.fillColor('white').fontSize(7).font('Helvetica-Bold').text(sev, 60, y + 3, { width: 32, align: 'center' });

      doc.fillColor(COLOR.ink).fontSize(10).font('Helvetica-Bold').text(r.label, 100, y, { width: doc.page.width - 160, ellipsis: true });

      const fixText = r.fix || r.message || '';
      doc.fillColor(COLOR.muted).fontSize(9).font('Helvetica').text(fixText, 100, y + 13, { width: doc.page.width - 160, height: 12, ellipsis: true });

      y += 32;
    }
    y += 6;
  }

  pageFooter(doc, 3);
}

// ---------- PAGE 4: WHAT'S WORKING + NEXT STEPS ----------
function closingPage(doc, audit) {
  doc.addPage();
  pageHeader(doc, "What's Working & Next Steps");

  let y = 110;
  const passed = audit.results.filter((r) => r.passed);

  if (passed.length) {
    doc.fillColor(COLOR.ink).fontSize(13).font('Helvetica-Bold').text(`What's working (${passed.length} checks passed)`, 60, y);
    y += 22;

    // 2 columns
    const colW = (doc.page.width - 130) / 2;
    const half = Math.ceil(passed.length / 2);
    const lineH = 14;
    const maxRows = Math.min(half, 12);
    passed.slice(0, maxRows).forEach((r, i) => {
      doc.fillColor(COLOR.green).fontSize(9).font('Helvetica').text(`✓  ${r.label}`, 60, y + i * lineH, { width: colW, ellipsis: true });
    });
    passed.slice(half, half + maxRows).forEach((r, i) => {
      doc.fillColor(COLOR.green).fontSize(9).font('Helvetica').text(`✓  ${r.label}`, 60 + colW + 10, y + i * lineH, { width: colW, ellipsis: true });
    });
    y += maxRows * lineH + 24;
  }

  // Next steps
  doc.fillColor(COLOR.ink).fontSize(13).font('Helvetica-Bold').text('Next steps', 60, y);
  y += 22;

  const steps = [
    { n: 1, title: 'Tackle critical issues first', desc: 'Critical issues are most likely to hurt search rankings — fix these before warnings.' },
    { n: 2, title: 'Re-audit after each batch of fixes', desc: 'Run seolens <url> after deploying changes to confirm the score improves.' },
    { n: 3, title: 'Add Seolens to your CI pipeline', desc: 'Seolens exits non-zero when score < 70 — gate deploys on SEO regressions.' },
  ];

  for (const s of steps) {
    doc.circle(72, y + 10, 11).fill(COLOR.primary);
    doc.fillColor('white').fontSize(11).font('Helvetica-Bold').text(`${s.n}`, 60, y + 5, { width: 24, align: 'center' });
    doc.fillColor(COLOR.ink).fontSize(11).font('Helvetica-Bold').text(s.title, 95, y + 1, { width: doc.page.width - 160 });
    doc.fillColor(COLOR.muted).fontSize(10).font('Helvetica').text(s.desc, 95, y + 17, { width: doc.page.width - 160, lineGap: 1 });
    y += 46;
  }

  // Pro upsell
  y += 10;
  const boxH = 90;
  if (y + boxH < doc.page.height - 70) {
    doc.roundedRect(60, y, doc.page.width - 120, boxH, 8).fill(COLOR.gray);
    doc.fillColor(COLOR.primary).fontSize(13).font('Helvetica-Bold').text('Want full-site audits?', 80, y + 14);
    doc.fillColor(COLOR.ink).fontSize(10).font('Helvetica').text(
      'Seolens Pro adds full-site crawl (up to 50,000 pages), Core Web Vitals, hreflang validation, AI fix suggestions, and white-label HTML reports.',
      80, y + 35, { width: doc.page.width - 160, lineGap: 2 },
    );
    doc.fillColor(COLOR.primary).fontSize(10).font('Helvetica-Bold').text('→  github.com/bluegalaxydev/seolens-pro', 80, y + 72);
  }

  pageFooter(doc, 4);
}

// ---------- HELPERS ----------
function pageHeader(doc, title) {
  doc.rect(0, 0, doc.page.width, 8).fill(COLOR.primary);
  doc.fillColor(COLOR.muted).fontSize(10).font('Helvetica-Bold').text('SEOLENS', 60, 28);
  doc.fillColor(COLOR.muted).fontSize(10).font('Helvetica').text('SEO Audit Report', 60, 28, { width: doc.page.width - 120, align: 'right' });
  doc.fillColor(COLOR.ink).fontSize(22).font('Helvetica-Bold').text(title, 60, 60);
  doc.moveTo(60, 95).lineTo(doc.page.width - 60, 95).strokeColor(COLOR.border).lineWidth(0.5).stroke();
}

function pageFooter(doc, pageNum) {
  doc.fillColor(COLOR.muted).fontSize(8).font('Helvetica').text(
    `Generated by Seolens · github.com/bluegalaxydev/seolens · Page ${pageNum} of 4`,
    60, doc.page.height - 35, { width: doc.page.width - 120, align: 'center' },
  );
}

function groupBy(arr, key) {
  const out = {};
  for (const x of arr) {
    if (!out[x[key]]) out[x[key]] = [];
    out[x[key]].push(x);
  }
  return out;
}

function severityColor(s) {
  if (s === 'critical' || s === 'error') return COLOR.red;
  if (s === 'warning') return COLOR.yellow;
  if (s === 'info') return COLOR.blue;
  return COLOR.muted;
}

function severityRank(s) {
  if (s === 'critical' || s === 'error') return 3;
  if (s === 'warning') return 2;
  if (s === 'info') return 1;
  return 0;
}

function scoreInterpretation(s) {
  if (s >= 90) return 'Excellent on-page SEO. The site follows current best practices across meta tags, structure, and technical fundamentals. Focus on monitoring and incremental improvements — the warnings noted below are polish, not blockers.';
  if (s >= 80) return 'Solid SEO foundation. A few warnings to clean up but nothing that should significantly hurt rankings. The fixes listed below are quick wins that will bump you into the A range.';
  if (s >= 70) return 'Acceptable but needs attention. Multiple warnings present. Fixing the top 3-5 high-impact items below will likely move you to a B grade and improve organic visibility within a few crawl cycles.';
  if (s >= 60) return 'Notable issues that may suppress organic visibility. Prioritize the critical findings below — these often represent the highest ROI fixes you can make right now.';
  return 'Significant on-page issues that are likely hurting search performance. Address critical findings immediately. Consider engaging an SEO specialist if you need help prioritizing.';
}
